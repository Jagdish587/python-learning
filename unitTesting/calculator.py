class  MyCalculator():
    
    def add(self, x, y):
        """ This is addition func """
        return x+y

    def sub(self,x,y):
        """ This is sub func """
        print("entered into sub func")
        return x-y

    def mul(self,x,y):
        """ This is mul func """
        return x*y

    def div(self,x,y):
        """ This is div func """
        return x/y

